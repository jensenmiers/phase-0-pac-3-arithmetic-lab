function add(x, y) {
    return x + y
}

function subtract(b,a) {
    return b - a
}

function multiply(a, b) {
    return a * b
}

function divide(a, b) {
    return a / b
}

function increment(n) {
    return n += 1
}

function decrement(n) {
    return n -= 1
}

function makeInt(n) {
    return parseInt(n, 10)

}

function preserveDecimal(string) {
    return parseFloat(string)
}